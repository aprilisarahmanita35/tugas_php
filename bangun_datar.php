<?php

	// 1. Menghitung luas persegi
	$sisi_persegi = 5;
	$luas_persegi = $sisi_persegi * $sisi_persegi;
	echo "Luas Persegi: $luas_persegi <br>";

	// 2. Menghitung luas persegi panjang
	$panjang = 8;
	$lebar = 4;
	$luas_persegi_panjang = $panjang * $lebar;
	echo "Luas Persegi Panjang: $luas_persegi_panjang <br>";

	// 3. Menghitung luas segitiga
	$alas = 6;
	$tinggi_segitiga = 9;
	$luas_segitiga = 0.5 * $alas * $tinggi_segitiga;
	echo "Luas Segitiga: $luas_segitiga <br>";

	// 4. Menghitung luas lingkaran
	$jari_jari = 5;
	$luas_lingkaran = 3.14 * $jari_jari * $jari_jari;
	echo "Luas Lingkaran: $luas_lingkaran <br>";

	// 5. Menghitung luas trapesium
	$panjang_atas = 6;
	$panjang_bawah = 8;
	$tinggi_trapesium = 5;
	$luas_trapesium = 0.5 * ($panjang_atas + $panjang_bawah) * $tinggi_trapesium;
	echo "Luas Trapesium: $luas_trapesium <br>";

?>
